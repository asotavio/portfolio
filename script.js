const rotatingWord = document.getElementById("rotating-word");
const languageButtons = document.querySelectorAll("[data-language]");
const translatableItems = document.querySelectorAll("[data-i18n]");
const metaDescription = document.getElementById("meta-description");
let currentLanguage = localStorage.getItem("portfolio-language") || "pt";
let wordIndex = 0;
let rotationTimer;

function updateRotatingWord() {
    if (!rotatingWord) {
        return;
    }

    const words = translations[currentLanguage].rotatingWords;
    rotatingWord.textContent = words[wordIndex % words.length];
}

function startWordRotation() {
    clearInterval(rotationTimer);
    updateRotatingWord();

    rotationTimer = setInterval(() => {
        const words = translations[currentLanguage].rotatingWords;
        wordIndex = (wordIndex + 1) % words.length;
        rotatingWord.textContent = words[wordIndex];
    }, 2200);
}

function applyTranslations(language) {
    const dictionary = translations[language];

    document.documentElement.lang = language === "pt" ? "pt-BR" : "en";
    document.title = dictionary.pageTitle;

    if (metaDescription) {
        metaDescription.setAttribute("content", dictionary.metaDescription);
    }

    translatableItems.forEach((item) => {
        const key = item.dataset.i18n;
        if (dictionary[key]) {
            item.textContent = dictionary[key];
        }
    });

    languageButtons.forEach((button) => {
        button.classList.toggle("is-active", button.dataset.language === language);
    });

    currentLanguage = language;
    localStorage.setItem("portfolio-language", language);
    wordIndex = 0;
    startWordRotation();
}

languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
        applyTranslations(button.dataset.language);
    });
});

applyTranslations(currentLanguage);

const revealItems = document.querySelectorAll(".reveal");

const observer = new IntersectionObserver(
    (entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add("is-visible");
                observer.unobserve(entry.target);
            }
        });
    },
    {
        threshold: 0.18
    }
);

revealItems.forEach((item) => observer.observe(item));
